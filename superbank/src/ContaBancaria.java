public class ContaBancaria{
    
    
    private String titular;
    private double saldoReais = 200;
    private double saldoDolar = 10000;
    private double saldoBtc = 0.0126;
    
    
    public String getTitular() {
        return titular;
    }
    public void setTitular(String titular) {
        this.titular = titular;
    }
    public double getSaldoReais() {
        return saldoReais;
    }
    public void setSaldoReais(double saldoReais) {
        this.saldoReais = saldoReais;
    }
    public double getSaldoDolar() {
        return saldoDolar;
    }
    public void setSaldoDolar(double saldoDolar) {
        this.saldoDolar = saldoDolar;
    }
    public double getSaldoBtc() {
        return saldoBtc;
    }
    public void setSaldoBtc(double saldoBtc) {
        this.saldoBtc = saldoBtc;
    }
    
    



}